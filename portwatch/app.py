def _lighten(hex_color: str, amount: float = 0.55) -> str:
    """Blend a hex colour toward white by *amount* (0 = original, 1 = white).

    Used for the halo ring behind each port bubble — a light tint of the
    country colour reads better than hard white on the satellite basemap.
    """
    h = hex_color.lstrip("#")
    r, g, b = (int(h[i:i + 2], 16) for i in (0, 2, 4))
    r = int(r + (255 - r) * amount)
    g = int(g + (255 - g) * amount)
    b = int(b + (255 - b) * amount)
    return f"#{r:02x}{g:02x}{b:02x}"


def _latlon_ring(
    center_lat: float, center_lon: float, radius_deg: float, n: int = 96,
) -> tuple[list[float], list[float]]:
    """Return (lats, lons) for a closed circle on the map.

    Corrects longitude spacing for latitude so the ring looks round on a
    Mercator-style projection. Used for the Hormuz chokepoint glow effect.
    """
    theta = np.linspace(0, 2 * np.pi, n)
    lon_scale = 1.0 / math.cos(math.radians(center_lat))
    lats = (center_lat + radius_deg * np.cos(theta)).tolist()
    lons = (center_lon + radius_deg * np.sin(theta) * lon_scale).tolist()
    return lats, lons


