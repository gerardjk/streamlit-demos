@st.cache_data(ttl=86400, show_spinner=False)
def load_coastline_geojson() -> dict:
    """Natural Earth 50m coastline for the map's vector coastline layer.

    Downloaded from GitHub on first run and cached locally so the app
    works offline afterwards. Returns a GeoJSON FeatureCollection dict
    that Plotly's mapbox layer can render directly.
    """
    local = DATA_DIR / "ne_50m_coastline.geojson"
    if not local.exists():
        url = (
            "https://raw.githubusercontent.com/nvkelso/natural-earth-vector/"
            "master/geojson/ne_50m_coastline.geojson"
        )
        try:
            with urllib.request.urlopen(url, timeout=10) as resp:
                local.write_bytes(resp.read())
        except Exception:
            return {"type": "FeatureCollection", "features": []}
    try:
        return json.loads(local.read_text())
    except Exception:
        return {"type": "FeatureCollection", "features": []}


@st.cache_data(ttl=86400, show_spinner=False)
def load_country_polygons() -> dict[str, dict]:
    """Natural Earth 50m country polygons for the faint per-country fill
    layer on the map.

    Returns a dict like {"Iran": <FeatureCollection>, ...} containing only
    the Gulf-region countries we shade. Splitting per-country at load time
    lets us add one mapbox fill layer per country with its own colour.
    """
    local = DATA_DIR / "ne_50m_admin_0_countries.geojson"
    if not local.exists():
        url = (
            "https://raw.githubusercontent.com/nvkelso/natural-earth-vector/"
            "master/geojson/ne_50m_admin_0_countries.geojson"
        )
        try:
            with urllib.request.urlopen(url, timeout=15) as resp:
                local.write_bytes(resp.read())
        except Exception:
            return {}
    try:
        full = json.loads(local.read_text())
    except Exception:
        return {}

    # Natural Earth stores country names under different property keys
    # depending on the vintage, so we check multiple fields.
    wanted = set(COUNTRY_COLORS.keys())
    aliases = {
        "United Arab Emirates": {"United Arab Emirates", "UAE"},
        "Saudi Arabia": {"Saudi Arabia"},
    }
    out: dict[str, dict] = {}
    for feat in full.get("features", []):
        props = feat.get("properties", {}) or {}
        names = {props.get("NAME"), props.get("ADMIN"), props.get("NAME_EN")}
        names.discard(None)
        for target in wanted:
            if target in names or names & aliases.get(target, set()):
                out.setdefault(target, {"type": "FeatureCollection", "features": []})
                out[target]["features"].append(feat)
                break
    return out


@st.cache_data(ttl=3600, show_spinner=False)
def _load_daily_ports_cached(mtime: float) -> pd.DataFrame:
    """Load daily port-call data with pre-computed 7-day rolling averages.

    The *mtime* argument is not used inside the function body — it exists
    so that Streamlit's cache key changes when the file is rewritten on
    disk (event-driven invalidation instead of time-based).
    """
    df = pd.read_parquet(DAILY_PORTS_PATH)
    df["date"] = pd.to_datetime(df["date"]).dt.normalize()
    df = df.sort_values(["portid", "date"])
    # Pre-compute rolling averages for each cargo column so map lookups
    # are instant regardless of which ship-type filter is active.
    roll_cols = ["portcalls"] + [c for c in DAILY_PORT_CARGO_COLS.values() if c in df.columns]
    for col in roll_cols:
        df[f"{col}_roll7"] = (
            df.groupby("portid", observed=True)[col]
              .transform(lambda s: s.rolling(7, min_periods=1).mean())
        )
    return df


def load_daily_ports() -> pd.DataFrame:
    """Thin wrapper that guards against a missing file, then passes the
    current mtime into the cached loader for cache invalidation."""
    if not DAILY_PORTS_PATH.exists():
        return pd.DataFrame()
    return _load_daily_ports_cached(DAILY_PORTS_PATH.stat().st_mtime)


@st.cache_data(ttl=3600, show_spinner=False)
def load_ports() -> pd.DataFrame:
    """Load Gulf port metadata. Converts CSV to Parquet on first read
    for faster subsequent loads."""
    if PORTS_PARQUET.exists():
        return pd.read_parquet(PORTS_PARQUET)
    if PORTS_CSV.exists():
        df = pd.read_csv(PORTS_CSV)
        df.to_parquet(PORTS_PARQUET, index=False)
        return df
    return pd.DataFrame()


@st.cache_data(ttl=3600, show_spinner=False)
def load_data() -> pd.DataFrame:
    """Load chokepoint transit data (Parquet preferred, CSV fallback).

    On first run, reads the CSV, filters to Hormuz, narrows dtypes
    (category for strings, int32 for counts), and writes a Parquet file.
    Every run after that short-circuits to the fast Parquet read.
    """
    if DATA_PARQUET.exists():
        return pd.read_parquet(DATA_PARQUET)
    if DATA_CSV.exists():
        df = pd.read_csv(DATA_CSV, parse_dates=["date"])
        df = df[df["chokepoint_name"].str.contains("Hormuz", case=False, na=False)]
        df["cargo_type"] = df["cargo_type"].astype("category")
        df["chokepoint_name"] = df["chokepoint_name"].astype("category")
        df["transits"] = df["transits"].astype("int32")
        df.to_parquet(DATA_PARQUET, index=False)
        return df
    st.error(
        f"No data at `{DATA_CSV}`. Run "
        "`python portwatch/fetch_data.py` to download IMF PortWatch data."
    )
    st.stop()


