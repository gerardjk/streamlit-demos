# ---------------------------------------------------------------------------
# Animated dashboard fragment
#
# @st.fragment isolates this section: when the play button is active,
# st.rerun() at the bottom only reruns this function, not the entire app.
# The header, CSS, filters, and aggregates above stay frozen, so only
# the map, charts, and KPIs re-render on each animation tick (~150ms).
# ---------------------------------------------------------------------------

@st.fragment
def render_dashboard():
    # --- Animation state ---
    SLIDER_KEY = "map_date_slider"
    if SLIDER_KEY not in st.session_state:
        st.session_state[SLIDER_KEY] = slider_max
    if "playing" not in st.session_state:
        st.session_state.playing = False

    # Clamp slider to current filter bounds (in case the user narrowed
    # the date range while animation was running).
    st.session_state[SLIDER_KEY] = max(slider_min, min(slider_max, st.session_state[SLIDER_KEY]))

    # Advance one day per tick when playing; wrap around at the end.
    if st.session_state.playing and slider_min != slider_max:
        nxt = st.session_state[SLIDER_KEY] + timedelta(days=1)
        st.session_state[SLIDER_KEY] = nxt if nxt <= slider_max else slider_min

    # --- Two-column layout: map on the left, charts on the right ---
    map_col, chart_col = st.columns([1, 1], gap="medium")

    # --- Charts (right column) ---
    with chart_col:
        # Play/pause button + date slider.
        if slider_min == slider_max:
            selected_day = slider_max
        else:
            play_col, slider_col = st.columns([1, 10], gap="small")
            with play_col:
                label = "\u23f8" if st.session_state.playing else "\u25b6"
                if st.button(label, key="play_btn", use_container_width=True,
                             help="Auto-advance the map date"):
                    st.session_state.playing = not st.session_state.playing
                    st.rerun()
            with slider_col:
                selected_day = st.slider(
                    "Map date",
                    min_value=slider_min,
                    max_value=slider_max,
                    format="YYYY-MM-DD",
                    step=timedelta(days=1),
                    key=SLIDER_KEY,
                    label_visibility="collapsed",
                )

        st.markdown(
            "<div class='chart-heading'>Daily transits through Hormuz</div>",
            unsafe_allow_html=True,
        )

        # Look up metrics for the currently selected day.
        selected_day_ts = pd.Timestamp(selected_day).normalize()
        day_row = daily[daily["date"] == selected_day_ts]
        day_val = float(day_row["transits"].iloc[0]) if not day_row.empty else 0.0
        day_rolling = float(day_row["rolling"].iloc[0]) if not day_row.empty else 0.0
        total_transits = int(fdf["transits"].sum())
        days_covered = int(daily.shape[0])

