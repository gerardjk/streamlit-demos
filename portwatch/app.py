def _breakdown_text(row: pd.Series) -> str:
    """Build a monospaced per-cargo-type bar chart for a single day's hover."""
    lines = []
    for col in cargo_order:
        val = int(row[col])
        filled = int(round(BAR_WIDTH * val / bar_max))
        bar = ("\u2588" * filled).ljust(BAR_WIDTH)
        lines.append(f"{col:<{LABEL_WIDTH}}{bar} {val:>4}")
    return "<br>".join(lines)


